package Orientacao_Objetos;

public class Aula08_OperadoresRelacionais {
    public static void main(String[] args) {    
    int valor1 = 1;
    int valor2 = 2; 
    
    System.out.println("valor1 == valor2:" + (valor1 == valor2));
    System.out.println("valor1 != valor2:" + (valor1 != valor2));
    System.out.println("valor1 > valor2 :" + (valor1 > valor2));
    System.out.println("valor1 < valor2 :" + (valor1 < valor2));    
    System.out.println("valor1 >= valor2 :" + (valor1 >= valor2));
    System.out.println("valor1 <= valor2 :" + (valor1 <= valor2));  
    }

}
